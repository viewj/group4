package group4_project;

public class Seller {
	private int sellCode;
	private String memberId;
	private String sellName;
	private String sellAddress;
	private String sellCategory;
	private String sellMenu;
	private String sellPrice;
	private String sellDate;
	private String sellContent;
	
	public int getSellCode() {
		return sellCode;
	}
	public void setSellCode(int sellCode) {
		this.sellCode = sellCode;
	}
	public String getMemberId() {
		return memberId;
	}
	public void setMemberId(String memberId) {
		this.memberId = memberId;
	}
	public String getSellName() {
		return sellName;
	}
	public void setSellName(String sellName) {
		this.sellName = sellName;
	}
	public String getSellAddress() {
		return sellAddress;
	}
	public void setSellAddress(String sellAddress) {
		this.sellAddress = sellAddress;
	}
	public String getSellCategory() {
		return sellCategory;
	}
	public void setSellCategory(String sellCategory) {
		this.sellCategory = sellCategory;
	}
	public String getSellMenu() {
		return sellMenu;
	}
	public void setSellMenu(String sellMenu) {
		this.sellMenu = sellMenu;
	}
	public String getSellPrice() {
		return sellPrice;
	}
	public void setSellPrice(String sellPrice) {
		this.sellPrice = sellPrice;
	}
	public String getSellDate() {
		return sellDate;
	}
	public void setSellDate(String sellDate) {
		this.sellDate = sellDate;
	}
	public String getSellContent() {
		return sellContent;
	}
	public void setSellContent(String sellContent) {
		this.sellContent = sellContent;
	}
	
}
