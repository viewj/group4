package group4_project;

public class Seller {
	private String sell_id;
	private String sell_name;
	private String sell_address;
	private String sell_category;
	private String sell_menu;
	private String sell_price;
	private String sell_date;
	private String sell_content;
	
	public String getSell_id() {
		return sell_id;
	}
	public void setSell_id(String sell_id) {
		this.sell_id = sell_id;
	}
	public String getSell_name() {
		return sell_name;
	}
	public void setSell_name(String sell_name) {
		this.sell_name = sell_name;
	}
	public String getSell_address() {
		return sell_address;
	}
	public void setSell_address(String sell_address) {
		this.sell_address = sell_address;
	}
	public String getSell_category() {
		return sell_category;
	}
	public void setSell_category(String sell_category) {
		this.sell_category = sell_category;
	}
	public String getSell_menu() {
		return sell_menu;
	}
	public void setSell_menu(String sell_menu) {
		this.sell_menu = sell_menu;
	}
	public String getSell_price() {
		return sell_price;
	}
	public void setSell_price(String sell_price) {
		this.sell_price = sell_price;
	}
	public String getSell_date() {
		return sell_date;
	}
	public void setSell_date(String sell_date) {
		this.sell_date = sell_date;
	}
	public String getSell_content() {
		return sell_content;
	}
	public void setSell_content(String sell_content) {
		this.sell_content = sell_content;
	}
	
	
	
}
